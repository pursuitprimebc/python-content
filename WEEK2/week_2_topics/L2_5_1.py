# escape characters in strings
print("it's a beautiful day")
print('it\'s a beautiful day')
print('we are from "iitm" madras')
print("we are from \"iitm\" madras")
print('my name is bibhash, i am from bihar.')
print('my name is bibhash,\t\t i am from bihar.')
print('my name is bibhash, \ni am from bihar.')







